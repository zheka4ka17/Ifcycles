package com.company;

public class Switch {
    public static void main(String[] args) {
        int mark = 4;
        switch (mark) {
            case 1:
                System.out.println("very bad");break;

            case 2:
                System.out.println("bad");break;
            case 3:
                System.out.println("satisfactoru");break;
            case 4:
                System.out.println("good");break;
            case 5:
                System.out.println("great");break;
            default:
                System.out.println("undefined");
        }
    }
}
